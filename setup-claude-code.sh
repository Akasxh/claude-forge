#!/bin/bash
# Claude Code Global Setup Script
# Installs global configs, agents, session prompts, and shell aliases
# Usage: chmod +x setup-claude-code.sh && ./setup-claude-code.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CLAUDE_DIR="$HOME/.claude"
CLAUDE_JSON="$HOME/.claude.json"
BASHRC="$HOME/.bashrc"

echo "=== Claude Code Global Setup ==="
echo ""

# Create directories
mkdir -p "$CLAUDE_DIR/agents"
mkdir -p "$CLAUDE_DIR/agents/research"
mkdir -p "$CLAUDE_DIR/session-prompts"
mkdir -p "$CLAUDE_DIR/lib"
mkdir -p "$CLAUDE_DIR/teams/research"

# ---- Helper: install file if not exists or differs ----
install_file() {
    local src="$1"
    local dest="$2"
    local label="$3"
    local force="${4:-false}"

    if [ "$force" = "true" ] || [ ! -f "$dest" ]; then
        cp "$src" "$dest"
        echo "[done] $label"
    elif cmp -s "$src" "$dest" 2>/dev/null; then
        echo "[ok]   $label (up to date)"
    else
        cp "$src" "$dest"
        echo "[upd]  $label (updated)"
    fi
}

# ---- Core configs ----
echo "Core configs:"
install_file "$SCRIPT_DIR/global-CLAUDE.md" "$CLAUDE_DIR/CLAUDE.md" "  ~/.claude/CLAUDE.md"
install_file "$SCRIPT_DIR/collaboration.md" "$CLAUDE_DIR/collaboration.md" "  ~/.claude/collaboration.md"
install_file "$SCRIPT_DIR/claude-code-bootstrap-prompt.md" "$CLAUDE_DIR/bootstrap-prompt.md" "  ~/.claude/bootstrap-prompt.md"

# ---- Settings (force-updated: we want bypassPermissions + PreToolUse git-identity hook) ----
if [ -f "$CLAUDE_DIR/settings.json" ]; then
    cp "$CLAUDE_DIR/settings.json" "$CLAUDE_DIR/settings.json.bak.$(date +%s)" 2>/dev/null || true
fi
install_file "$SCRIPT_DIR/global-settings.json" "$CLAUDE_DIR/settings.json" "  ~/.claude/settings.json" true

# ---- MCP config ----
if [ -f "$CLAUDE_JSON" ]; then
    if python3 -c "import json; d=json.load(open('$CLAUDE_JSON')); assert 'mcpServers' in d and len(d['mcpServers']) > 0" 2>/dev/null; then
        echo "[ok]   ~/.claude.json (has MCP servers)"
    else
        cp "$CLAUDE_JSON" "$CLAUDE_JSON.bak"
        python3 -c "
import json
with open('$CLAUDE_JSON') as f: existing = json.load(f)
with open('$SCRIPT_DIR/global-mcp-config.json') as f: new_mcp = json.load(f)
existing['mcpServers'] = new_mcp.get('mcpServers', {})
with open('$CLAUDE_JSON', 'w') as f: json.dump(existing, f, indent=2)
"
        echo "[done] ~/.claude.json (merged MCP servers)"
    fi
else
    install_file "$SCRIPT_DIR/global-mcp-config.json" "$CLAUDE_JSON" "  ~/.claude.json"
fi

# ---- Agents (top level) ----
echo ""
echo "Agents:"
for agent_file in "$SCRIPT_DIR/agents/"*.md; do
    [ -f "$agent_file" ] || continue
    agent_name=$(basename "$agent_file")
    install_file "$agent_file" "$CLAUDE_DIR/agents/$agent_name" "  $agent_name"
done

# ---- Research Team (leader + 10 specialists, all opus) ----
echo ""
echo "Research Team:"
for agent_file in "$SCRIPT_DIR/agents/research/"*.md; do
    [ -f "$agent_file" ] || continue
    agent_name=$(basename "$agent_file")
    install_file "$agent_file" "$CLAUDE_DIR/agents/research/$agent_name" "  research/$agent_name"
done

# ---- Team protocols ----
echo ""
echo "Team protocols:"
install_file "$SCRIPT_DIR/teams/research/PROTOCOL.md" "$CLAUDE_DIR/teams/research/PROTOCOL.md" "  teams/research/PROTOCOL.md"

# ---- Library scripts (git-identity, etc.) ----
echo ""
echo "Library scripts:"
install_file "$SCRIPT_DIR/lib/git-identity.sh" "$CLAUDE_DIR/lib/git-identity.sh" "  lib/git-identity.sh"
chmod +x "$CLAUDE_DIR/lib/git-identity.sh" 2>/dev/null || true

# ---- Session prompts ----
echo ""
echo "Session prompts:"
for prompt_file in "$SCRIPT_DIR/session-prompts/"*.md; do
    [ -f "$prompt_file" ] || continue
    prompt_name=$(basename "$prompt_file")
    install_file "$prompt_file" "$CLAUDE_DIR/session-prompts/$prompt_name" "  $prompt_name"
done

# ---- Shell integration ----
echo ""
SHELL_BLOCK_START="# >>> claude-code-setup >>>"
SHELL_BLOCK_END="# <<< claude-code-setup <<<"

SHELL_BLOCK="$SHELL_BLOCK_START
# Claude Code multi-session aliases
# Auto-generated by setup-claude-code.sh — edit the setup script, not this block

claude-orchestrator() {
    claude --append-system-prompt-file \"\$HOME/.claude/session-prompts/orchestrator.md\" \"\$@\"
}

claude-worker() {
    claude --append-system-prompt-file \"\$HOME/.claude/session-prompts/worker.md\" \"\$@\"
}

claude-reviewer() {
    claude --append-system-prompt-file \"\$HOME/.claude/session-prompts/reviewer.md\" \"\$@\"
}

claude-tester() {
    claude --append-system-prompt-file \"\$HOME/.claude/session-prompts/tester.md\" \"\$@\"
}

claude-init() {
    # Initialize collaboration structure in current project
    if [ ! -f \"CLAUDE.md\" ] && [ ! -f \".claude/CLAUDE.md\" ]; then
        echo \"No CLAUDE.md found. Run 'claude-orchestrator' to bootstrap the project first.\"
        return 1
    fi
    mkdir -p .claude/coordination
    echo \"# Task Board\" > .claude/coordination/TASKS.md
    echo \"\" >> .claude/coordination/TASKS.md
    echo \"## Backlog\" >> .claude/coordination/TASKS.md
    echo \"\" >> .claude/coordination/TASKS.md
    echo \"## In Progress\" >> .claude/coordination/TASKS.md
    echo \"\" >> .claude/coordination/TASKS.md
    echo \"## Review\" >> .claude/coordination/TASKS.md
    echo \"\" >> .claude/coordination/TASKS.md
    echo \"## Done\" >> .claude/coordination/TASKS.md
    echo \"# Activity Log\" > .claude/coordination/LOG.md
    echo \"# Review Queue\" > .claude/coordination/REVIEWS.md
    echo \"\"
    echo \"Collaboration initialized in .claude/coordination/\"
    echo \"\"
    echo \"Start terminals:\"
    echo \"  Terminal 1: claude-orchestrator\"
    echo \"  Terminal 2: claude-worker\"
    echo \"  Terminal 3: claude-worker\"
    echo \"  Terminal 4: claude-reviewer  (optional)\"
}

claude-status() {
    # Show current collaboration state
    if [ -f .claude/coordination/TASKS.md ]; then
        echo \"=== TASK BOARD ===\"
        cat .claude/coordination/TASKS.md
        echo \"\"
        echo \"=== RECENT LOG ===\"
        tail -15 .claude/coordination/LOG.md 2>/dev/null || echo \"(empty)\"
    else
        echo \"No active collaboration. Run 'claude-init' or 'claude-orchestrator' to start.\"
    fi
}

claude-clean() {
    # Clean up coordination files after a session
    if [ -d .claude/coordination ]; then
        rm -rf .claude/coordination
        echo \"Coordination files removed.\"
    else
        echo \"No coordination files to clean.\"
    fi
}
$SHELL_BLOCK_END"

if grep -qF "$SHELL_BLOCK_START" "$BASHRC" 2>/dev/null; then
    # Remove old block and replace
    python3 -c "
import re
with open('$BASHRC') as f: content = f.read()
pattern = r'$SHELL_BLOCK_START.*?$SHELL_BLOCK_END'
cleaned = re.sub(pattern, '', content, flags=re.DOTALL).rstrip()
with open('$BASHRC', 'w') as f: f.write(cleaned + '\n')
"
    echo "$SHELL_BLOCK" >> "$BASHRC"
    echo "[upd]  Shell aliases in ~/.bashrc (updated)"
else
    echo "" >> "$BASHRC"
    echo "$SHELL_BLOCK" >> "$BASHRC"
    echo "[done] Shell aliases in ~/.bashrc"
fi

echo ""
echo "=== Setup Complete ==="
echo ""
echo "Shell commands available (run 'source ~/.bashrc' or open a new terminal):"
echo ""
echo "  claude-orchestrator  Start as orchestrator (analyzes project, creates plan)"
echo "  claude-worker        Start as worker (claims and implements tasks)"
echo "  claude-reviewer      Start as reviewer (reviews completed work)"
echo "  claude-tester        Start as tester (writes and runs tests)"
echo "  claude-init          Initialize collaboration files in current project"
echo "  claude-status        Show current task board and recent log"
echo "  claude-clean         Remove coordination files after done"
echo ""
echo "Typical workflow:"
echo "  cd ~/my-project"
echo "  claude-orchestrator          # Terminal 1: plan the work"
echo "  # orchestrator tells you what to do next..."
echo "  claude-worker                # Terminal 2: auto-claims tasks"
echo "  claude-worker                # Terminal 3: auto-claims tasks"
echo "  claude-reviewer              # Terminal 4: reviews completed work"
echo ""
echo "Run: source ~/.bashrc"
